"""
Generate workspace-level Graphify-style artifacts for CDS dependency tracing.

Writes:
  <workspace-root>/graphify-out/graph.json
  <workspace-root>/graphify-out/GRAPH_REPORT.md

Run from any cwd:
  python scripts/dev/generate_graphify_out.py
"""
from __future__ import annotations

import ast
import json
import sys
from collections import Counter, defaultdict
from dataclasses import dataclass
from pathlib import Path


def _inner_repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _workspace_root() -> Path:
    # inner .../bank-diligence-platform/bank-diligence-platform -> outer .../bank-diligence-platform
    return Path(__file__).resolve().parents[3]


def _module_id_from_path(inner_root: Path, py_path: Path) -> str | None:
    try:
        rel = py_path.relative_to(inner_root / "backend" / "app")
    except ValueError:
        return None
    if rel.name == "__init__.py":
        parts = rel.parent.parts
    else:
        parts = rel.parent.parts + (rel.stem,)
    if not parts or parts == ("",):
        return "app"
    return "app." + ".".join(parts)


@dataclass
class FileInfo:
    module_id: str
    path: str


def _collect_import_targets(tree: ast.AST) -> set[str]:
    targets: set[str] = set()

    for node in ast.walk(tree):
        if isinstance(node, ast.ImportFrom):
            if node.module is None:
                continue
            if node.module == "app" or node.module.startswith("app."):
                if node.module == "app":
                    for alias in node.names:
                        if alias.name == "*":
                            continue
                        targets.add(f"app.{alias.name}")
                else:
                    targets.add(node.module)
        elif isinstance(node, ast.Import):
            for alias in node.names:
                name = alias.name
                if name == "app" or name.startswith("app."):
                    targets.add(name)
    return targets


def build_graph(inner_root: Path) -> tuple[list[dict], list[dict], list[FileInfo]]:
    app_root = inner_root / "backend" / "app"
    files: list[FileInfo] = []
    edges: list[dict] = []
    seen_modules: set[str] = set()

    for py_path in sorted(app_root.rglob("*.py")):
        if "__pycache__" in py_path.parts:
            continue
        mod_id = _module_id_from_path(inner_root, py_path)
        if mod_id is None:
            continue
        seen_modules.add(mod_id)
        files.append(FileInfo(module_id=mod_id, path=str(py_path.relative_to(inner_root))))

        try:
            source = py_path.read_text(encoding="utf-8")
        except OSError:
            continue
        try:
            tree = ast.parse(source, filename=str(py_path))
        except SyntaxError:
            continue
        for tgt in _collect_import_targets(tree):
            if tgt == mod_id:
                continue
            edges.append({"from": mod_id, "to": tgt, "kind": "imports"})

    nodes = [{"id": m, "label": m, "path": next((f.path for f in files if f.module_id == m), None)} for m in sorted(seen_modules)]
    return nodes, edges, files


def write_report(
    out_dir: Path,
    nodes: list[dict],
    edges: list[dict],
) -> None:
    in_degree = Counter(e["to"] for e in edges)
    out_degree = Counter(e["from"] for e in edges)
    missing_targets = sorted({e["to"] for e in edges} - {n["id"] for n in nodes})

    lines = [
        "# GRAPH_REPORT (CDS)",
        "",
        "Auto-generated by `bank-diligence-platform/scripts/dev/generate_graphify_out.py`.",
        "Edges are **static** `app.*` imports between Python modules under `backend/app/`.",
        "",
        "## Summary",
        "",
        f"- **Nodes (modules with files):** {len(nodes)}",
        f"- **Edges (import references):** {len(edges)}",
        f"- **Unresolved import targets (no matching file under app/):** {len(missing_targets)}",
        "",
    ]

    if missing_targets:
        lines.append("## Unresolved targets (risk / typo hints)")
        lines.append("")
        lines.append("These modules are imported but have no corresponding file under `backend/app/`.")
        lines.append("")
        for m in missing_targets[:80]:
            lines.append(f"- `{m}`")
        if len(missing_targets) > 80:
            lines.append(f"- … and {len(missing_targets) - 80} more")
        lines.append("")

    lines.append("## Hub modules (high outbound references)")
    lines.append("")
    for mod, c in out_degree.most_common(15):
        lines.append(f"- `{mod}` → {c} outbound edge(s)")
    lines.append("")

    lines.append("## Heavily depended-on modules (high inbound references)")
    lines.append("")
    for mod, c in in_degree.most_common(15):
        lines.append(f"- `{mod}` ← {c} inbound edge(s)")
    lines.append("")

    (out_dir / "GRAPH_REPORT.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> int:
    inner = _inner_repo_root()
    workspace = _workspace_root()
    out_dir = workspace / "graphify-out"
    out_dir.mkdir(parents=True, exist_ok=True)

    if not (inner / "backend" / "app").is_dir():
        print("Expected backend/app under inner repo:", inner, file=sys.stderr)
        return 1

    nodes, edges, _files = build_graph(inner)
    payload = {
        "generator": "generate_graphify_out.py",
        "inner_repo": str(inner),
        "nodes": nodes,
        "edges": edges,
    }
    (out_dir / "graph.json").write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
    write_report(out_dir, nodes, edges)
    print("Wrote", out_dir / "graph.json")
    print("Wrote", out_dir / "GRAPH_REPORT.md")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
